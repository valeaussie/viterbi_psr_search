#!/usr/bin/env python
"""
gen_search_params.py

Generate a search.params file for viterbi_pipeline.py from a PRESTO .inf file.

Reads tsamp and the start MJD directly from the .inf so they are always
correct and consistent with the dedispersed time series being analysed
(barycentric start MJD for a barycentred .dat, topocentric for a topo .dat).

All other search parameters are fixed here to the values agreed for the
47 Tuc O re-recovery test, except Tsft and out_prefix which are passed in.

Usage
-----
    python gen_search_params.py \
        --inf   <path to .inf> \
        --dat   <path to .dat> \
        --out   <path to write search_<mode>.params> \
        --tsft  450 \
        --out-prefix exp_01_bary \
        --dm    24.356441
"""

import argparse
import os
import sys


def read_inf(inf_path):
    """Parse the few fields we need from a PRESTO .inf file."""
    tsamp = None
    mjd_start = None
    with open(inf_path, "r", errors="replace") as fh:
        for line in fh:
            low = line.lower()
            if "width of each time series bin" in low:
                try:
                    tsamp = float(line.split("=")[-1].strip())
                except (ValueError, IndexError):
                    pass
            elif "epoch of observation (mjd)" in low:
                try:
                    mjd_start = float(line.split("=")[-1].strip())
                except (ValueError, IndexError):
                    pass
    return tsamp, mjd_start


def main():
    p = argparse.ArgumentParser()
    p.add_argument("--inf", required=True, help="Path to PRESTO .inf file.")
    p.add_argument("--dat", required=True, help="Path to PRESTO .dat file (infile).")
    p.add_argument("--out", required=True, help="Path to write the params file.")
    p.add_argument("--tsft", type=float, required=True,
                   help="Tsft (s) i.e. coherent segment length for this run.")
    p.add_argument("--out-prefix", required=True,
                   help="out_prefix for the viterbi_pipeline.py outputs.")
    p.add_argument("--dm", type=float, default=24.356441,
                   help="DM (pc cm^-3), passed through to the params file.")
    # Fixed search parameters (overridable if ever needed)
    p.add_argument("--f0", type=float, default=378.2)
    p.add_argument("--bw", type=float, default=0.2)
    p.add_argument("--padding-factor", type=float, default=1)
    p.add_argument("--top-paths", type=int, default=1)
    p.add_argument("--num-harm", type=int, default=1)
    args = p.parse_args()

    if not os.path.exists(args.inf):
        sys.exit(f"ERROR: .inf not found: {args.inf}")
    if not os.path.exists(args.dat):
        sys.exit(f"ERROR: .dat not found: {args.dat}")

    tsamp, mjd_start = read_inf(args.inf)
    if tsamp is None:
        sys.exit(f"ERROR: could not read tsamp from {args.inf}")
    if mjd_start is None:
        sys.exit(f"ERROR: could not read start MJD from {args.inf}")

    lines = [
        "#------- Auto-generated by gen_search_params.py. Do not edit by hand.",
        f"#------- Source .inf: {args.inf}",
        "#------- Path to the input time-series data file",
        f'infile = "{args.dat}"',
        "#------- Sampling time of the input time series (s), read from .inf",
        f"tsamp = {tsamp:.14e}",
        "#------- Duration of each SFT segment (s); df ~ 1/Tsft",
        f"Tsft = {args.tsft:g}",
        "#------- Number of SFT segments (-1 = maximum that fits)",
        "Nsft = -1",
        "#------- Central frequency of the search band (Hz)",
        f"f0 = {args.f0:g}",
        "#------- Total bandwidth of the search (Hz)",
        f"bw = {args.bw:g}",
        "#------- Zero-padding factor before the FFT (1 = none)",
        f"padding_factor = {args.padding_factor:g}",
        "#------- Prefix for all output files",
        f'out_prefix = "{args.out_prefix}"',
        "#------- Plot the Viterbi frequency path after the search",
        "plot_path = True",
        "#------- Number of highest-likelihood Viterbi paths to return",
        f"top_paths = {args.top_paths}",
        "#------- Save the full Viterbi delta matrix to disk",
        "save_delta = False",
        "#------- Number of harmonics to include (1 = fundamental only)",
        f"num_harm = {args.num_harm}",
        "#------- DM (pc cm^-3), used for the psrfold candfile",
        f"dm = {args.dm:.6f}",
        "#------- Start MJD of the dedispersed time series, read from .inf",
        "#------- (barycentric for a barycentred .dat, topocentric otherwise)",
        f"mjd_start = {mjd_start:.15f}",
    ]

    os.makedirs(os.path.dirname(os.path.abspath(args.out)), exist_ok=True)
    with open(args.out, "w") as fh:
        fh.write("\n".join(lines) + "\n")

    print(f"Wrote {args.out}")
    print(f"  infile     = {args.dat}")
    print(f"  tsamp      = {tsamp:.14e}")
    print(f"  mjd_start  = {mjd_start:.15f}")
    print(f"  Tsft       = {args.tsft:g}")
    print(f"  out_prefix = {args.out_prefix}")


if __name__ == "__main__":
    main()